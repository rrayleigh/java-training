package training;

public class SolvingExpressions {
	public static void main(String[] arg){
		
		/*Assuming that the integer variable x is 5 and the integer variable y is 10.*/
		
		int x,y;
		x=5;
		y=10;
		
		System.out.println("The result of expression \"x + y * 2\" is :" +(x + y * 2));
		System.out.println("The result of expression \"x - y + 2\" is :" +(x - y + 2));
		System.out.println("The result of expression \"(x+y) * 2\" is :" +((x+y) * 2));
		System.out.println("The result of expression \"y % x\" is :" +(y % x));
	}

}
